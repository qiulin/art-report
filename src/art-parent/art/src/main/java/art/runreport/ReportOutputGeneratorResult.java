package art.runreport;

/**
 * Represents a report output generator result
 * 
 * @author Timothy Anyona
 */
public class ReportOutputGeneratorResult {
	private String message;
	private boolean success;
	private Integer rowCount;
	private boolean tooManyRows;

	/**
	 * @return the tooManyRows
	 */
	public boolean isTooManyRows() {
		return tooManyRows;
	}

	/**
	 * @param tooManyRows the tooManyRows to set
	 */
	public void setTooManyRows(boolean tooManyRows) {
		this.tooManyRows = tooManyRows;
	}

	/**
	 * @return the message
	 */
	public String getMessage() {
		return message;
	}

	/**
	 * @param message the message to set
	 */
	public void setMessage(String message) {
		this.message = message;
	}

	/**
	 * @return the success
	 */
	public boolean isSuccess() {
		return success;
	}

	/**
	 * @param success the success to set
	 */
	public void setSuccess(boolean success) {
		this.success = success;
	}

	/**
	 * @return the rowCount
	 */
	public Integer getRowCount() {
		return rowCount;
	}

	/**
	 * @param rowCount the rowCount to set
	 */
	public void setRowCount(Integer rowCount) {
		this.rowCount = rowCount;
	}
	

}
